class Gallery extends Component {
  state = {
    initialImage: imagesList[0].imageUrl,
    initialId: imagesList[0],
  }

  imageClicked = id => {
    this.setState({
      initialImage: imagesList[id].imageUrl,
      initialId: imagesList[id],
    })
  }

  render() {
    const {initialImage, initialId} = this.state
    return (
      <div className="app-container">
        <div className="semiApp-container">
          <img
            src={initialImage}
            className="galleryimage-size"
            alt="{imagesList[]}"
          />
          <h1>Nature Photography</h1>
          <p>Nature Photo by Kiran</p>

          <ul className="thumbnail-container">
            {imagesList.map(eachImage => (
              <ThumbnailItem
                eachDetails={eachImage}
                key={eachImage.id}
                onclickimage={this.imageClicked}
                isActive={eachImage.id === initialId}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
